# Gene-co-expression-network-analysis
This repository includes scripts and workflows for analyzing the gene co-expression networks.

## Method
Weighted Gene Co-expression Network Analysis (WGCNA)

Reference:
Langfelder, P., & Horvath, S. (2008). WGCNA: an R package for weighted correlation network analysis. 

## Analysis Workflow
1. Import gene expression data
2. Construct co-expression modules
3. Visualization

## Tools
- R
- WGCNA package

